#Faça um Programa que peça um valor e mostre na tela se o valor é positivo ou negativo.


positivo = input("Insira numero: ")

if positivo >= -0:
    print ("numero positivo: %d") % (positivo)
else:
    print("Numero negativo: %d") % (positivo)
